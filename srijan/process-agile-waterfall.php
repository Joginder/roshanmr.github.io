            <section class="article-subsection" id="agileWaterfall">
              <h4>Pre-Engagement</h4>
              <h1>Choosing Agile Development Over Waterfall</h1>
              <header class="sub-header">
                <?php echo file_get_contents("img/team.svg"); ?>
              </header>
              <p>Srijan follows Agile methodologies for product development. This method offers a far more efficient way to create products, than the traditional waterfall method which gives little value to building the right product.</p>

              <p>The waterfall method is a step-by-step, linear approach to development. Only when one step is completed, does the next one start. And the process enforces a rigid adherence to the features list, architecture and design. It’s only after the entire coding and internal testing has been completed that the client or user gets to test it.</p>

              <p>But can a product’s features have full-fledged, fixed descriptions when development starts? No! No one, not even the product owner, can have a full, in-depth, detailed view of what the product really is, and does. At best, it can start out as a vision detailed out with features that are likely to be valuable to end users. And it evolves as it gets developed in parts.</p>

              <p>This is what we mean when we say that the waterfall method pays scant attention to building the right product. By the time the user gets to test the product, many months have passed, and what gets built could be different from what the user wanted. There’s little time, room or scope for iterations or improvements, or even to figure out if the product being built is the right one or not.</p>

              <p>In the Agile development services, the entire process after the first stage of requirements gathering is cyclical. It’s an iterative process of discovery, build and user testing that helps Srijan and the product company in finalizing the features and functions that are right for the end user.</p>

              <p>Two-week sprints are defined in which blocks of the product get built and tested. At end of almost every two weeks the client gets to see a demo of the features built and test it. This means that very early on in the development, we get to know if the product is on the right track. If it’s not, we get to amend it and bring it back on track, rapidly.</p>

              <p>The Agile methodology gives the product team the flexibility to evolve the product, and the discipline to build it in a timely manner. This means that during a demo, it could be discovered that a new feature needs to be built. This may require going back to the architecture and design stage, which then means redefining the product backlogs and estimates, and so on.</p>

              <p><em>The Agile methodology gives the product team the flexibility to evolve the product, and the discipline to build it in a timely manner</em></p>

            </section>


